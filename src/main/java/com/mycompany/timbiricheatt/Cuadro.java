    private void btnIzqSupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIzqSupMouseClicked
        // TODO add your handling code here:
        int x = evt.getX();
        int y = evt.getY();
        int w = btnIzqSup.getWidth();
        int h = btnIzqSup.getHeight();
        
        int distLeft = x;
        int distRight = w - x;
        int distTop = y;
        int distBottom = h - y;

        int min = Math.min(Math.min(distLeft, distRight), Math.min(distTop, distBottom));

        String donde = "valioverga";
        if (min == distLeft) {
            donde = "izq";
            this.izq = true;
        } else if (min == distRight) {
            donde = "der";
            this.der = true;
        } else if (min == distTop) {
            donde = "arriba";
            this.arriba = true;
        } else {
            donde = "abajo";
            this.abajo = true;
        }
        System.out.println("Me picaron en " + donde + ", soy = " + num);
    }//GEN-LAST:event_btnIzqSupMouseClicked

