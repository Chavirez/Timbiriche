    private void renderizarCuadro(ImageIcon icono){
        
        Image overlay = icono.getImage();
        super.getGraphics().drawImage(overlay, 0, 0, getWidth(), getHeight(), this);
        
        if(der){
       
            super.getGraphics().setColor(color);
            super.getGraphics().drawLine(getWidth() - 1, 0, getWidth() - 1, getHeight());
        
        }
            
        if(izq){
       
            super.getGraphics().setColor(color);
            super.getGraphics().drawLine(0, 0, 0, getHeight());
        
        }
            
        if(abajo){
            
            super.getGraphics().setColor(color);
            super.getGraphics().drawLine(0, getHeight()-1, getWidth(), getHeight()-1);
        
        }
            
        if(arriba){
       
            super.getGraphics().setColor(color);
            super.getGraphics().drawLine(0, 0, getWidth(), 0);
        
        }
        
        
    }
